o = 0
s = 0
m = []
a1 = 0
a2 = 0
print('Введите числа: ')
while True:
    n = int(input())
    if n == 0:
        break
    o += 1
    s += n
    m.append(n)
    if (n % 2) == 0:
        a1 += 1
    else:
        a2 += 1
k = s / o
print(f'Кол-во чисел: {o}, сумма чисел: {s}, среднее арифметическое чисел: {k:.2f}, минимальное значение: {min(m)},\
 максимальное значение: {max(m)}, кол-во четных чисел: {a1}, нечетных: {a2}.')