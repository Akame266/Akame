a = input('Введите строку: ')
print(a[2], '- третий символ в строке')
print(a[-2], '- предпоследний символ строки')
print(a[:5], '- первые пять символов строки')
print(a[:-2], '- вся строка, кроме последних двух символов')
print(a[0::2], '- все символы с четными индексами')
print(a[1::2], '- все символы с нечетными индексами')
print(a[::-1], '- все символы в обратном порядке')
print(a[-1::-2], '- все символы строки через один в обратном порядке, начиная с последнего')
print(len(a), '- длина данной строки')