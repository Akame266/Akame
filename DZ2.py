a = int(input('Количество учеников в А классе = '))
b = int(input('Количество учеников в Б классе = '))
v = int(input('Количество учеников в В классе = '))
a1 = (a//2+a%2)
print(f'Необходимое количество парт для класс A составляет {a1} шт.')
b1 = (b//2+b%2)
print(f'Необходимое количество парт для класс Б составляет {b1} шт.')
v1 = (v//2+v%2)
print(f'Необходимое количество парт для класс В составляет {v1} шт.')
c = a1+b1+v1
print(f'Необходимое количество парт для всех трех классов составляет {c} шт.')